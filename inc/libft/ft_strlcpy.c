#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	len_s;

	len_s = 0;
	if (src == NULL || dest == NULL || !size)
		return (ft_strlen(src));
	i = 0;
	while (src[i] != '\0' && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	while (src[len_s] != '\0')
	{
		len_s++;
	}
	return (len_s);
}
